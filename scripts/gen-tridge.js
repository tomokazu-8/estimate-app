// ===== 得意先マスタ（実績DBから読み込み + 既存Tridgeを保持）=====
function buildClientRows() {
  const header = ['得意先ID','枝番','得意先名','郵便番号','住所','代表電話','代表メール','担当者名','担当者電話','担当者メール','担当者メモ'];

  let existingRows = [];
  const existingNames = new Set();
  let maxId = 0;

  // 既存Tridgeから引き継ぎ
  if (fs.existsSync(OUT_KOSHU)) {
    try {
      const existWb = XLSX.readFile(OUT_KOSHU);
      const ws = existWb.Sheets['得意先マスタ'];
      if (ws) {
        existingRows = XLSX.utils.sheet_to_json(ws, { header: 1 }).slice(1);
        existingRows.forEach(r => {
          const name = String(r[2] || '').trim();
          if (name) existingNames.add(name);
          const num = parseInt(String(r[0] || '').replace(/\D/g, '')) || 0;
          if (num > maxId) maxId = num;
        });
        console.log('既存Tridgeから得意先', existingRows.length, '社を引き継ぎ');
      }
    } catch(e) {
      console.warn('既存Tridge読み込みスキップ:', e.message);
    }
  }

  // 実績DBから新規得意先を追記
  let addedCount = 0;
  if (fs.existsSync(DB_PATH)) {
    try {
      const dbWb = XLSX.readFile(DB_PATH);
      const ws = dbWb.Sheets['物件マスタ'];
      if (ws) {
        const rows = XLSX.utils.sheet_to_json(ws);
        rows.forEach(r => {
          const name = String(r['得意先'] || '').trim();
          if (!name || existingNames.has(name)) return;
          maxId++;
          existingRows.push(['cl' + String(maxId).padStart(3, '0'), 0, name, '', '', '', '', '', '', '', '']);
          existingNames.add(name);
          addedCount++;
        });
        if (addedCount > 0) console.log('実績DBから新規得意先', addedCount, '社を追加');
      }
    } catch(e) {
      console.warn('実績DB読み込みスキップ:', e.message);
    }
  }

  if (existingRows.length === 0) {
    console.log('得意先マスタ: サンプルデータを追加');
    existingRows = [
      ['cl001', 0, 'サンプル建設株式会社', '', '', '', '', '', '', '', ''],
    ];
  }

  return [header, ...existingRows];
}

